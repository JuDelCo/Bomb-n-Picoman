function player_input(pid, input)
	local p = nil

	for player in all(players) do
		if player.id == pid then
			p = player
		end
	end

	if p == nil then
		return
	end

	if btn(0, input) then -- left
		p.dx = -1
	elseif btn(1, input) then -- right
		p.dx = 1
	else
		p.dx = 0
	end

	if btn(2, input) then -- up
		p.dy = -1
	elseif btn(3, input) then -- down
		p.dy = 1
	else
		p.dy = 0
	end

	if btnp(4, input) then -- z
		local player_bombs = 0
		for bomb in all(bombs) do
			if bomb.id == p.id then
				player_bombs = player_bombs + 1
			end
		end
		if player_bombs < p.u.bombs then
			for bomb in all(bombs) do
				if bomb.x == p.x and bomb.y == p.y then
					return
				end
			end
			create_bomb(p.id, p.x, p.y, p.u.power, 90)
		end
	end
end

function cpu_input(pid)
	local p = nil

	for player in all(players) do
		if player.id == pid then
			p = player
		end
	end

	if p == nil then
		return
	end

	-- ultra retard mode, only weird moves

	if rnd(100) > 95 then
		p.dx = 1
	elseif rnd(100) > 95 then
		p.dx = -1
	elseif rnd(100) > 95 then
		p.dx = 0
	end

	if rnd(100) > 95 then
		p.dy = 1
	elseif rnd(100) > 95 then
		p.dy = -1
	elseif rnd(100) > 95 then
		p.dy = 0
	end
end

function player_move(p)
	local dx = p.dx
	local dy = p.dy

	if p.dx > 0 then
		if cell_is_solid(p.x + 1, p.y) then
			if p.oy >= 2 and not cell_is_solid(p.x + 1, p.y + 1) then
				if p.dy != -1 then
					p.dy = 1
				end
			elseif p.oy <= -2 and not cell_is_solid(p.x + 1, p.y - 1) then
				if p.dy != 1 then
					p.dy = -1
				end
			end
		elseif p.oy > 0 and cell_is_solid(p.x + 1, p.y + 1) then
			p.dx = 0
			if p.dy != 1 then
				p.dy = -1
			end
		elseif p.oy < 0 and cell_is_solid(p.x + 1, p.y - 1) then
			p.dx = 0
			if p.dy != -1 then
				p.dy = 1
			end
		end
	elseif p.dx < 0 then
		if cell_is_solid(p.x - 1, p.y) then
			if p.oy >= 2 and not cell_is_solid(p.x - 1, p.y + 1) then
				if p.dy != -1 then
					p.dy = 1
				end
			elseif p.oy <= -2 and not cell_is_solid(p.x - 1, p.y - 1) then
				if p.dy != 1 then
					p.dy = -1
				end
			end
		elseif p.oy > 0 and cell_is_solid(p.x - 1, p.y + 1) then
			p.dx = 0
			if p.dy != 1 then
				p.dy = -1
			end
		elseif p.oy < 0 and cell_is_solid(p.x - 1, p.y - 1) then
			p.dx = 0
			if p.dy != -1 then
				p.dy = 1
			end
		end
	elseif p.dy > 0 then
		if cell_is_solid(p.x, p.y + 1) then
			if p.ox >= 2 and not cell_is_solid(p.x + 1, p.y + 1) then
				if p.dx != -1 then
					p.dx = 1
				end
			elseif p.ox <= -2 and not cell_is_solid(p.x - 1, p.y + 1) then
				if p.dx != 1 then
					p.dx = -1
				end
			end
		elseif p.ox > 0 and cell_is_solid(p.x + 1, p.y + 1) then
			p.dy = 0
			if p.dx != 1 then
				p.dx = -1
			end
		elseif p.ox < 0 and cell_is_solid(p.x - 1, p.y + 1) then
			p.dy = 0
			if p.dx != -1 then
				p.dx = 1
			end
		end
	elseif p.dy < 0 then
		if cell_is_solid(p.x, p.y - 1) then
			if p.ox >= 2 and not cell_is_solid(p.x + 1, p.y - 1) then
				if p.dx != -1 then
					p.dx = 1
				end
			elseif p.ox <= -2 and not cell_is_solid(p.x - 1, p.y - 1) then
				if p.dx != 1 then
					p.dx = -1
				end
			end
		elseif p.ox > 0 and cell_is_solid(p.x + 1, p.y - 1) then
			p.dy = 0
			if p.dx != 1 then
				p.dx = -1
			end
		elseif p.ox < 0 and cell_is_solid(p.x - 1, p.y - 1) then
			p.dy = 0
			if p.dx != -1 then
				p.dx = 1
			end
		end
	end

	if p.dx > 0 and p.ox >= 0 and cell_is_bomb(p.x + 1, p.y) then
		p.dx = 0
	elseif p.dx < 0 and p.ox <= 0 and cell_is_bomb(p.x - 1, p.y) then
		p.dx = 0
	end

	if p.dy > 0 and p.oy >= 0 and cell_is_bomb(p.x, p.y + 1) then
		p.dy = 0
	elseif p.dy < 0 and p.oy <= 0 and cell_is_bomb(p.x, p.y - 1) then
		p.dy = 0
	end

	if p.oy == 0 then
		if dy != 0 and p.ox != 0 and sgn(p.ox) != sgn(p.ox + (p.dx * p.u.speed)) then
			p.ox = 0
		else
			p.ox = p.ox + (p.dx * p.u.speed)
		end

		if p.dx > 0 then
			p.dir = 1
		elseif p.dx < 0 then
			p.dir = 3
		end
	end

	if p.ox > 0 and cell_is_solid(p.x + 1, p.y) then
		p.ox = 0
	elseif p.ox < 0 and cell_is_solid(p.x - 1, p.y) then
		p.ox = 0
	end

	if p.ox == 0 then
		if dx != 0 and p.oy != 0 and sgn(p.oy) != sgn(p.oy + (p.dy * p.u.speed)) then
			p.oy= 0
		else
			p.oy = p.oy + (p.dy * p.u.speed)
		end

		if p.dy > 0 then
			p.dir = 0
		elseif p.dy < 0 then
			p.dir = 2
		end
	end

	if p.oy > 0 and cell_is_solid(p.x, p.y + 1) then
		p.oy = 0
	elseif p.oy < 0 and cell_is_solid(p.x, p.y - 1) then
		p.oy = 0
	end

	if abs(p.ox) > 4 then
		local sign = sgn(p.ox)
		p.x = p.x + sign
		p.ox = (4 - abs(p.ox - (4 * sign))) * -sign

		if fget(mget(p.x , p.y), 3) then
			if p.u.power < 12 then
				p.u.power = p.u.power + 1
				mset(p.x , p.y, 64)
			end
		elseif fget(mget(p.x , p.y), 4) then
			if p.u.speed < 1.8 then
				p.u.speed = p.u.speed + 0.2
				mset(p.x , p.y, 64)
			end
		elseif fget(mget(p.x , p.y), 5) then
			if p.u.bombs < 10 then
				p.u.bombs = p.u.bombs + 1
				mset(p.x , p.y, 64)
			end
		end
	elseif abs(p.oy) > 4 then
		local sign = sgn(p.oy)
		p.y = p.y + sign
		p.oy = (4 - abs(p.oy - (4 * sign))) * -sign

		if fget(mget(p.x , p.y), 3) then
			if p.u.power < 12 then
				p.u.power = p.u.power + 1
				mset(p.x , p.y, 64)
			end
		elseif fget(mget(p.x , p.y), 4) then
			if p.u.speed < 1.8 then
				p.u.speed = p.u.speed + 0.2
				mset(p.x , p.y, 64)
			end
		elseif fget(mget(p.x , p.y), 5) then
			if p.u.bombs < 10 then
				p.u.bombs = p.u.bombs + 1
				mset(p.x , p.y, 64)
			end
		end
	end
end

function player_fire(p)
	altposx = p.x
	altposy = p.y

	if abs(p.ox) > 2 then
		altposx = p.x + 1 * sgn(p.ox)
	end

	if abs(p.oy) > 2 then
		altposy = p.y + 1 * sgn(p.oy)
	end

	for fire in all(fires) do
		if (p.x == fire.x and p.y == fire.y) or (altposx == fire.x and altposy == fire.y) then
			destroy_player(p)
			break
		end
	end
end

function bomb_time(b)
	b.timer = b.timer - 1
	if b.timer <= 0 then
		destroy_bomb(b)
	end
end

function fire_time(f)
	f.timer = f.timer - 1
	if f.timer <= 0 then
		del(fires, f)
	end
end

function time_destroy_map()
	if lvl.time > 40 then
		return
	end

	lvl.d.t = lvl.d.t + 1

	if lvl.d.t >= 8.4 then
		lvl.d.t = lvl.d.t - 8.4

		create_wall(lvl.d.posx, lvl.d.posy)
		sfx(3)

		if lvl.d.dir == 0 then
			if lvl.d.posx >= lvl.d.right then
				lvl.d.dir = 1
				lvl.d.right = lvl.d.right - 1
				lvl.d.posy = lvl.d.posy - 1
			else
				lvl.d.posx = lvl.d.posx + 1
			end
		elseif lvl.d.dir == 1 then
			if lvl.d.posy <= lvl.d.up then
				lvl.d.dir = 2
				lvl.d.up = lvl.d.up + 1
				lvl.d.posx = lvl.d.posx - 1
			else
				lvl.d.posy = lvl.d.posy - 1
			end
		elseif lvl.d.dir == 2 then
			if lvl.d.posx <= lvl.d.left then
				lvl.d.dir = 3
				lvl.d.left = lvl.d.left + 1
				lvl.d.posy = lvl.d.posy + 1
			else
				lvl.d.posx = lvl.d.posx - 1
			end
		elseif lvl.d.dir == 3 then
			if lvl.d.posy >= lvl.d.down then
				lvl.d.dir = 0
				lvl.d.down = lvl.d.down - 1
				lvl.d.posx = lvl.d.posx + 1
			else
				lvl.d.posy = lvl.d.posy + 1
			end
		end
	end
end

function time_tick()
	t.f = t.f + 1
	if t.f == 30 then
		t.s = t.s + 1
		t.f = 0
		if lvl.time > 0 then
			lvl.time = lvl.time - 1
		end
	end
end

