function create_player(id, x, y, col)
	local e = {}
	e.id = id
	e.x = x
	e.y = y
	e.ox = 0
	e.oy = 0
	e.dx = 0
	e.dy = 0
	e.dir = 0
	e.w = 4
	e.h = 4
	e.col = col
	e.u = {}
	e.u.power = 1
	e.u.speed = 0.8
	e.u.bombs = 1
	add(players, e)
	return e
end

function create_bomb(pid, x, y, range, delay)
	local e = {}
	e.id = pid
	e.x = x
	e.y = y
	e.range = range
	e.timer = delay
	add(bombs, e)
	sfx(0)
	return e
end

function create_fire(x, y, type, delay)
	if cell_is_wall(x, y) then
		return nil
	end

	if cell_is_powerup(x, y) then
		mset(x, y, 64)
	end

	for bomb in all(bombs) do
		if bomb.x == x and bomb.y == y then
			destroy_bomb(bomb)
		end
	end

	local e = {}
	e.x = x
	e.y = y
	e.type = type
	e.timer = delay
	add(fires, e)
	return e
end

function create_box(x, y)
	if not cell_is_solid(x, y) then
		mset(x, y, 67)
	end
end

function create_wall(x, y)
	mset(x, y, 66)
	for player in all(players) do
		if player.x == x and player.y == y then
			destroy_player(player)
		end
	end
	for bomb in all(bombs) do
		if bomb.x == x and bomb.y == y then
			destroy_bomb(bomb)
		end
	end
	for fire in all(fires) do
		if fire.x == x and fire.y == y then
			del(fires, fire)
		end
	end
end

function destroy_player(p)
	del(players, p)
	sfx(2)

	while p.u.power > 1 or p.u.bombs > 1 or p.u.speed > 0.8 do
		for x=1,13 do
			for y=1,11 do
				if not cell_is_solid(x, y) and
						not cell_is_powerup(x, y) and
						not cell_is_bomb(x, y) then
					if rnd(100) > 95 then
						if p.u.power > 1 then
							p.u.power = p.u.power - 1
							mset(x, y, 80)
						elseif p.u.bombs > 1 then
							p.u.bombs = p.u.bombs - 1
							mset(x, y, 82)
						elseif p.u.speed > 0.8 then
							p.u.speed = p.u.speed - 0.2
							mset(x, y, 81)
						end
					end
				end
			end
		end
	end

	if count(players) == 1 then
		for player in all(players) do
			score[player.id] = score[player.id] + 1
		end
		restart_match()
	end
end

function destroy_bomb(b)
	del(bombs, b)
	lvl.shake = 10
	sfx(1)
	create_fire(b.x, b.y, 5, 20)

	local i = 1
	while i <= b.range do
		if i == b.range or cell_is_box(b.x + i, b.y) or cell_is_wall(b.x + i + 1, b.y) then
			create_fire(b.x + i, b.y, 8, 20)
		else
			create_fire(b.x + i, b.y, 7, 20)
		end

		if cell_is_solid(b.x + i, b.y) then
			if cell_is_box(b.x + i, b.y) then
				destroy_box(b.x + i, b.y)
			end
			break
		end

		i = i + 1
	end

	i = 1
	while i <= b.range do
		if i == b.range or cell_is_box(b.x - i, b.y) or cell_is_wall(b.x - i - 1, b.y) then
			create_fire(b.x - i, b.y, 9, 20)
		else
			create_fire(b.x - i, b.y, 7, 20)
		end

		if cell_is_solid(b.x - i, b.y) then
			if cell_is_box(b.x - i, b.y) then
				destroy_box(b.x - i, b.y)
			end
			break
		end

		i = i + 1
	end

	i = 1
	while i <= b.range do
		if i == b.range or cell_is_box(b.x, b.y + i) or cell_is_wall(b.x, b.y + i + 1) then
			create_fire(b.x, b.y + i, 10, 20)
		else
			create_fire(b.x, b.y + i, 6, 20)
		end

		if cell_is_solid(b.x, b.y + i) then
			if cell_is_box(b.x, b.y + i) then
				destroy_box(b.x, b.y + i)
			end
			break
		end

		i = i + 1
	end

	i = 1
	while i <= b.range do
		if i == b.range or cell_is_box(b.x, b.y - i) or cell_is_wall(b.x, b.y - i - 1) then
			create_fire(b.x, b.y - i, 11, 20)
		else
			create_fire(b.x, b.y - i, 6, 20)
		end

		if cell_is_solid(b.x, b.y - i) then
			if cell_is_box(b.x, b.y - i) then
				destroy_box(b.x, b.y - i)
			end
			break
		end

		i = i + 1
	end
end

function destroy_box(x, y)
	if cell_is_box(x, y) then
		mset(x, y, 64) -- floor

		local rand = rnd(100)

		if rand >= 20 and rand <= 35 then
			mset(x, y, 80) -- power
		elseif rand >= 50 and rand <= 65 then
			mset(x, y, 81) -- speed
		elseif rand >= 70 and rand <= 85 then
			mset(x, y, 82) -- bombs
		end
	end
end

