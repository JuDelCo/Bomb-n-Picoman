function cell_is_bomb(x, y)
	for bomb in all(bombs) do
		if bomb.x == x and bomb.y == y then
			return true
		end
	end
	return false
end

function cell_is_solid(x, y)
	-- walls and boxes
	return fget(mget(x, y), 1)
end

function cell_is_wall(x, y)
	return cell_is_solid(x, y) and not cell_is_box(x, y)
end

function cell_is_box(x, y)
	return fget(mget(x, y), 2)
end

function cell_is_powerup(x, y)
	return (fget(mget(x, y), 3) or fget(mget(x, y), 4) or fget(mget(x, y), 5))
end

































