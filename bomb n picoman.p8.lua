function reset_game()
	restart_match()
	score = { 0, 0, 0, 0 }
end

function restart_match()
	lvl.time = 120

	lvl.d.t = 0
	lvl.d.posx = 0
	lvl.d.posy = 11
	lvl.d.dir = 0
	lvl.d.left = 1
	lvl.d.right = 13
	lvl.d.up = 1
	lvl.d.down = 10

	clean_map()
	generate_boxes()

	players = {}
	bombs = {}
	fires = {}

	player_ids = {}
	cpu_ids = {}

	if player_count > 0 then
		add(player_ids, 1)
	else
		add(cpu_ids, 1)
	end

	if player_count > 1 then
		add(player_ids, 2)
	else
		add(cpu_ids, 2)
	end

	add(cpu_ids, 3)
	add(cpu_ids, 4)

	create_player(1,  1,  1, 12)
	create_player(2, 13,  1,  1)
	create_player(3,  1, 11,  4)
	create_player(4, 13, 11,  9)
end

function clean_map()
	for x=1,13 do
		for y=1,11 do
			if x % 2 == 0 and y % 2 == 0 then
				mset(x, y, 66)
			else
				mset(x, y, 64)
			end
		end
	end
end

function generate_boxes()
	for x=1,13 do
		for y=1,11 do
			if mget(x, y) == 67 then
				mset(x, y, 64) -- floor
			end
			if rnd(10) > 3.5 then
				if  not (x>=1 and  x<=2  and y>=1  and y<=2) and
					not (x>=12 and x<=13 and y>=1  and y<=2) and
					not (x>=1  and x<=2  and y>=10 and y<=11) and
					not (x>=12 and x<=13 and y>=10 and y<=11) then
					create_box(x, y)
				end
			end
		end
	end
end

