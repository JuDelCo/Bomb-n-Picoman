function _init()
	t = {}
	t.s = 0
	t.f = 0

	lvl = {}
	lvl.d = {}
	lvl.sx = 15
	lvl.sy = 15
	lvl.ox = 4
	lvl.oy = 8
	lvl.time = 0
	lvl.shake = 0

	player_count = 1

	reset_game()
end

function _update()
	if btnp(5, 0) then -- x (reset p1) temp!
		--restart_match()
		for x=1,13 do for y=1,11 do destroy_box(x, y) end end
	end

	if count(player_ids) > 0 then
		player_input(player_ids[1], 0)
	else
		cpu_input(cpu_ids[4])
	end

	if count(player_ids) > 1 then
		player_input(player_ids[2], 1)
	else
		cpu_input(cpu_ids[3])
	end

	cpu_input(cpu_ids[1])
	cpu_input(cpu_ids[2])

	foreach(players, player_move)
	foreach(players, player_fire)
	foreach(bombs, bomb_time)
	foreach(fires, fire_time)
	time_destroy_map()
	time_tick()
end

function _draw()
	rectfill(0, 0, 128, 128, 0) -- much faster than cls()

	if lvl.shake > 0 then
		lvl.shake = lvl.shake - 1
		camera(rnd(2) - 1, rnd(2) - 1)
	else
		camera()
	end

	local sec = ""
	if lvl.time % 60 < 10 then
		sec = "0"..(lvl.time % 60)
	else
		sec = (lvl.time % 60)
	end

	print("time: "..(flr(lvl.time / 60))..":"..sec, 44, 0, 7)

	map(0, 0, lvl.ox, lvl.oy, lvl.sx, lvl.sy)

	for bomb in all(bombs) do
		spr(4, bomb.x * 8 + lvl.ox, bomb.y * 8 + lvl.oy)
	end

	for fire in all(fires) do
		spr(fire.type, fire.x * 8 + lvl.ox, fire.y * 8 + lvl.oy)
	end

	palt(0, false) -- black eyes
	palt(10, true) -- yellow background
	for player in all(players) do
		pal(5, player.col)
		local flipx = false
		if player.dir == 3 then flipx = true end
		local psprite = 1
		if player.dir == 1 or player.dir == 3 then psprite = 2 end
		if player.dir == 2 then psprite = 3 end
		spr(psprite, player.x * 8 + player.ox + lvl.ox, player.y * 8 + player.oy + lvl.oy, 1, 1, flipx, false)
	end
	pal()

	for i=1,4 do
		local tc = 7
		if players[i] == nil then
			tc = 5
		end
		local s = score[i]
		if s < 10 then s = " "..score[i] end
		print("p"..i..":"..s, ((i - 1) * 4 * 9), 117, tc)
		local col = 12
		if i == 2 then col = 1
		elseif i == 3 then col = 4
		elseif i == 4 then col = 9
		end
		rectfill(((i - 1) * 4 * 9), 123, ((i - 1) * 4 * 9) + 18, 124, col)
	end

	-- 256 kb in manual, but its 512 kb on v0.1.1 !
	--print("cpu "..(flr(stat(1)*100)).."% ram "..flr(stat(0)).."/512 kb".." t "..t.s.."."..t.f, 0, 0, 7)
	--print("p.ox "..p.ox.." p.oy "..p.oy.." p.dx "..p.dx.." p.dy "..p.dy, 0, 117, 7)
end

